// ─────────────────────────────────────────────────────────────
// Shared SVG icon set for the v2 app screens.
//
// Every icon is a thin <svg> wrapper around the same 24×24 viewBox.
// Callers pass `size`, `stroke`, and `strokeWidth` to match the exact
// look they need (the v2 screens use a wide range of sizes/strokes),
// so these defaults are just sensible fallbacks. Any extra props
// (className, style, aria-*) are spread onto the <svg>.
// ─────────────────────────────────────────────────────────────

function Svg({ size = 16, stroke = "currentColor", strokeWidth = 2.5, fill = "none", children, ...rest }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={fill}
      stroke={stroke}
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      {...rest}
    >
      {children}
    </svg>
  );
}

